package com.nothing.object;

public class Relations {

	private String uid = "";
	private String friends = "";
	private String blacks = "";
	private String strangers = "";
	private String groups = "";
	private String recents = "";
	public String getUid() {
		return uid;
	}
	public void setUid(String uid) {
		this.uid = uid;
	}
	public String getFriends() {
		return friends;
	}
	public void setFriends(String friends) {
		this.friends = friends;
	}
	public String getBlacks() {
		return blacks;
	}
	public void setBlacks(String blacks) {
		this.blacks = blacks;
	}
	public String getStrangers() {
		return strangers;
	}
	public void setStrangers(String strangers) {
		this.strangers = strangers;
	}
	public String getGroups() {
		return groups;
	}
	public void setGroups(String groups) {
		this.groups = groups;
	}
	public String getRecents() {
		return recents;
	}
	public void setRecents(String recents) {
		this.recents = recents;
	}
	
}
